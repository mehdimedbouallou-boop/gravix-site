const langToggle = document.getElementById("langToggle");
const frLabel = document.getElementById("frLabel");
const arLabel = document.getElementById("arLabel");
const menuToggle = document.getElementById("menuToggle");
const navMenu = document.getElementById("navMenu");
const year = document.getElementById("year");

if (year) {
  year.textContent = new Date().getFullYear();
}

function setLanguage(lang) {
  const html = document.documentElement;
  const body = document.body;

  html.setAttribute("lang", lang);
  html.setAttribute("dir", lang === "ar" ? "rtl" : "ltr");

  if (lang === "ar") {
    body.classList.add("ar");
    if (frLabel) frLabel.classList.remove("active");
    if (arLabel) arLabel.classList.add("active");
    document.title = "Gravix | النقش بالليزر الحديث والمخصص";
  } else {
    body.classList.remove("ar");
    if (frLabel) frLabel.classList.add("active");
    if (arLabel) arLabel.classList.remove("active");
    document.title = "Gravix | Gravure laser moderne et personnalisée";
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  localStorage.setItem("gravix-language", lang);
}

if (langToggle) {
  langToggle.addEventListener("click", () => {
    const currentLang = document.documentElement.getAttribute("lang");
    const newLang = currentLang === "fr" ? "ar" : "fr";
    setLanguage(newLang);
  });
}

if (menuToggle && navMenu) {
  menuToggle.addEventListener("click", () => {
    navMenu.classList.toggle("open");
  });
}

document.querySelectorAll(".nav a").forEach((link) => {
  link.addEventListener("click", () => {
    if (navMenu) {
      navMenu.classList.remove("open");
    }
  });
});

const savedLang = localStorage.getItem("gravix-language") || "fr";
setLanguage(savedLang);

/* Prévisualisation des images pour les produits à variantes */
const productPreviews = document.querySelectorAll(".product-preview");

productPreviews.forEach((preview) => {
  const imageElement = preview.querySelector(".catalog-image");
  if (!imageElement) return;

  const images = (preview.dataset.images || "")
    .split(",")
    .map((item) => item.trim())
    .filter(Boolean);

  if (images.length <= 1) return;

  let hoverTimer = null;
  let slideInterval = null;
  let currentIndex = 0;
  let longPressTriggered = false;

  function showNextImage() {
    currentIndex = (currentIndex + 1) % images.length;
    imageElement.src = images[currentIndex];
  }

  function startPreview() {
    preview.classList.add("is-active");
    if (slideInterval) return;
    slideInterval = setInterval(showNextImage, 500);
  }

  function stopPreview() {
    preview.classList.remove("is-active");
    clearInterval(slideInterval);
    slideInterval = null;
    currentIndex = 0;
    imageElement.src = images[0];
  }

  function schedulePreview() {
    clearTimeout(hoverTimer);
    hoverTimer = setTimeout(() => {
      startPreview();
    }, 350);
  }

  function cancelScheduledPreview() {
    clearTimeout(hoverTimer);
  }

  preview.addEventListener("mouseenter", () => {
    schedulePreview();
  });

  preview.addEventListener("mouseleave", () => {
    cancelScheduledPreview();
    stopPreview();
  });

  preview.addEventListener(
    "touchstart",
    () => {
      longPressTriggered = false;
      clearTimeout(hoverTimer);
      hoverTimer = setTimeout(() => {
        longPressTriggered = true;
        startPreview();
      }, 350);
    },
    { passive: true }
  );

  preview.addEventListener("touchend", () => {
    cancelScheduledPreview();
    if (slideInterval) {
      setTimeout(() => {
        stopPreview();
      }, 120);
    }
  });

  preview.addEventListener("touchcancel", () => {
    cancelScheduledPreview();
    stopPreview();
  });

  preview.addEventListener("click", (event) => {
    if (longPressTriggered) {
      event.preventDefault();
      longPressTriggered = false;
    }
  });
});